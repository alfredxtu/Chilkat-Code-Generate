		# Other values are: 1. Hard Bounce. The email could not be delivered and BounceAddress contains the
		# failed email address. 2. Soft Bounce. A temporary condition exists causing the email delivery to
		# fail. The BounceAddress property contains the failed email address. 3. General Bounced Mail, cannot
		# determine if it is hard or soft, and the email address is not available. 4. General Bounced Mail,
		# cannot determine if it is hard or soft, but an email address is available. 5. Mail Block. A bounce
		# occured because the sender was blocked. 6. Auto-Reply/Out-of-Office email. 7. Transient message,
		# such as "Delivery Status / No Action Required". 8. Subscribe request. 9. Unsubscribe request. 10.
		# Virus email notification. 11. Suspected Bounce, but no other information is available 12.
		# Challenge/Response - Auto-reply message sent by SPAM software where only verified email addresses
		# are accepted. 13. Address Change Notification Messages. 
		# 14. Success DSN indicating that the
		# message was successfully relayed.
		# 15. Abuse/fraud feedback report.
		#
		# @return [Bignum]
		def get_BounceType() end

